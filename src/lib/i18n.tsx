export type UIText = (typeof uiText)[SupportedLocale];

export function LocaleProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<SupportedLocale>(() => getInitialLocale());

  useEffect(() => {
    document.documentElement.lang = locale;
    setStoredLocale(locale);
  }, [locale]);

  const value = useMemo(
    () => ({
      locale,
      setLocale: setLocaleState
    }),
    [locale]
  );

  return <LocaleContext.Provider value={value}>{children}</LocaleContext.Provider>;
}

export function useLocale() {
  const context = useContext(LocaleContext);

  if (!context) {
    return {
      locale: DEFAULT_LOCALE,
      setLocale: () => undefined
    };
  }

  return context;
}

export function useT() {
  const { locale } = useLocale();

  return uiText[locale];
}

export function getUIText(locale: SupportedLocale = DEFAULT_LOCALE) {
  return uiText[locale];
}

export function getSupportedLocaleOptions() {
  return supportedLocales.map((locale) => ({
    label: localeLabels[locale],
    locale,
    name: getLocaleName(locale)
  }));
}

export function translateDomainValue(locale: SupportedLocale, value: string) {
  const dictionary = uiText[locale].domain as Record<string, string>;

  return dictionary[value] ?? value;
}

export function translateGrade(locale: SupportedLocale, grade: string) {
  return translateDomainValue(locale, `Grade ${grade}`);
}
